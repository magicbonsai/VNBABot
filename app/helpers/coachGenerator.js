function getRandomInt(min, max) {
  min = Math.ceil(min);
  max = Math.floor(max);
  return Math.floor(Math.random() * (max - min) + min); //The maximum is exclusive and the minimum is inclusive
}

const getMax = object => {
  let max = Math.max(...Object.values(object));
  return Object.keys(object).filter(key => object[key] == max);
};

function generateCoach() {
  const doc = new GoogleSpreadsheet(process.env.GOOGLE_SHEETS_KEY);
  const { retiredPlayers: rPlayersId, availableCoaches: coachesId } = sheetIds;
  return (async function main() {
    await doc.useServiceAccountAuth({
      client_email: process.env.GOOGLE_SERVICE_ACCOUNT_EMAIL,
      private_key: process.env.GOOGLE_PRIVATE_KEY.replace(/\\n/g, "\n")
    });
    await doc.loadInfo();
    const sheets = doc.sheetsById;
    const retiredPlayersSheet = sheets[rPlayersId];
    const coachesSheet = sheets[coachesId];
    const retiredPlayers = await retiredPlayersSheet.getRows();
    const chosenPlayer = _.sample(retiredPlayers);
    const name = chosenPlayer.Name;
    const filteredFaces = [...cyberFaces];
    _.remove(
      [...cyberFaces],
      face =>
        face.includes("girl") ||
        face.includes("woman") ||
        face.includes("kid") ||
        face.includes("female")
    );
    const vitals = {
      module: "STAFF",
      tab: "VITALS",
      data: {
        FIRSTNAME: name.split(" ")[0],
        LASTNAME: name.split(" ")[1],
        FACEID: _.sample(filteredFaces).slice(0, 4)
      }
    };
    const offense = getRandomInt(6, 13);
    const attributes = {
      module: "STAFF",
      tab: "ATTRIBUTES",
      data: {
        OFFENSE: offense.toString(),
        DEFENSE: (18 - offense).toString()
      }
    };

    const prof = {
      Balanced: getRandomInt(50, 100),
      "Grit & Grind": getRandomInt(50, 100),
      "Pace & Space": getRandomInt(50, 100),
      "Perimeter Centeric": getRandomInt(50, 100),
      "Post Centric": getRandomInt(50, 100),
      Triangle: getRandomInt(50, 100),
      "Seven Seconds": getRandomInt(50, 100),
      Defense: getRandomInt(50, 100)
    };

    const system = systems.indexOf(getMax(prof)[0]);
    const style = {
      module: "STAFF",
      tab: "STYLE",
      data: {
        "STYLE_N#1": getRandomInt(1, 14).toString(),
        "STYLE_N#2": getRandomInt(1, 14).toString(),
        "STYLE_N#3": getRandomInt(1, 14).toString(),
        OFFENSE_VS_DEFENSE: getRandomInt(0, 6).toString(),
        GUARDS_VS_FORWARDS: getRandomInt(0, 6).toString(),
        INSIDE_VS_OUTSIDE: getRandomInt(0, 6).toString(),
        PREFERRED_SYSTEM: system.toString(),
        ACTIVE_SYSTEM: system.toString(),
        BALANCED_PROFICIENCY: prof.Balanced.toString(),
        "GRIT_&_GRIND_PROFICIENCY": prof["Grit & Grind"].toString(),
        "PACE_&_SPACE_PROFICIENCY": prof["Pace & Space"].toString(),
        PERIMETER_CENTERIC_PROFICIENCY: prof["Perimeter Centeric"].toString(),
        POST_CENTRIC_PROFICIENCY: prof["Post Centric"].toString(),
        TRIANGLE_PROFICIENCY: prof.Triangle.toString(),
        SEVEN_SECONDS_PROFICIENCY: prof["Seven Seconds"].toString(),
        DEFENSE_PROFICIENCY: prof.Defense.toString()
      }
    };
    (async () => {
      await coachesSheet.addRow({
        "Coach Name": name,
        Data: JSON.stringify([vitals, attributes, style]),
        Playbook: _.sample(nbaTeams),
        "Zone Usage": getRandomInt(20, 100),
        "Run Plays": getRandomInt(50, 100),
        "Performance Consideration": getRandomInt(1, 100),
        "Defensive Aggression": _.sample(defensiveAggression),
        "Defensive Focus": _.sample(defensiveFocus),
        "Offensive Focus": _.sample(offensiveFocus),
        "Offensive Rebounding": _.sample(oRebounding),
        "Defensive Rebounding": _.sample(dRebounding),
        "Offensive Tempo": _.sample(oTempo),
        "Bench Utilization": getRandomInt(1, 100),
        "Help Defense": getRandomInt(1, 100)
      });
    })();
  })();
}

